    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String bienSoXe = txtTKBienSoXe.getText();
        String tkVeXe = txtTKVeXe.getText();
        List<BaiXe> list = null;
        BaiXeDao dao = new BaiXeDao();
        try {
            if (bienSoXe.equals("") && tkVeXe.equals("")) {
                list = dao.findAll();
            } else if (!bienSoXe.equals("") && tkVeXe.equals("")) {
                list = timKiemBienSoXe(bienSoXe);
            } else if (bienSoXe.equals("") && !tkVeXe.equals("")) {
                list = timKiemTheXe(tkVeXe);
            } else {
                List<BaiXe> listTemp = timKiemBienSoXe(bienSoXe);
                list = listTemp.stream()
                        .filter(bx -> bx.getTheXe().equals(tkVeXe))
                        .collect(Collectors.toCollection(ArrayList::new));
            }
            tblModel.setRowCount(0);
            SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
            for (BaiXe bx : list) {
                tblModel.addRow(new Object[]{
                    bx.getTheXe(),
                    bx.getBienSoXe(),
                    format.format(bx.getThoiGianVao()),
                    bx.getLoaiXe(),
                    bx.getLoaiVe()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception ex) {
            System.out.println(ex);
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

