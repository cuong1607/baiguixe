    private void veThang(){
        try{
            VeThangDao dao = new VeThangDao();
            List<VeThang> list = dao.findAll();
            tblModel.setRowCount(0);
            for (VeThang vt : list) {
                tblModel.addRow(new Object[]{
                vt.getTheXe(), vt.getBienSoXe(), vt.getLoaiXe(), vt.getNgayDangKi(), vt.getNgayKetThuc(), vt.getThanhToan()});
            }
            tblModel.fireTableDataChanged();
        }      
    catch (Exception e){
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");   
        }
    }
    

