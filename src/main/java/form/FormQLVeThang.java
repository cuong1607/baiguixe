    private void btnGiaHanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGiaHanActionPerformed

        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        VeThang vt = null;
        VeThangDao dao = new VeThangDao();
        try {
            vt = dao.findByBienSoXe(txtBienSoXe.getText());
            if (vt != null) {
                Integer option = MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn gia hạn không?", "Hỏi");
                if (option == JOptionPane.OK_OPTION) {
                    long oneMonth = (long)30 * 24 * 60 * 60 * 1000;
                    Date date = new Date(new Date().getTime() + oneMonth);
                    int thanhToan = vt.getThanhToan();
                    if (vt.getLoaiXe().equals(LoaiXe.OTO.getName())) {
                        thanhToan += LoaiXe.OTO.getThang();
                    } else {
                        thanhToan += LoaiXe.XEMAY.getThang();
                    }
                    vt.setNgayKetThuc(date);
                    vt.setThanhToan(thanhToan);
                    if (dao.update(vt)) {
                        MessageDialogHelper.showMessageDialog(parentForm, "Vé tháng đã được cập nhật", "Thông báo");
                        txtNgayKetThuc.setText(format.format(vt.getNgayKetThuc()));
                        txtThanhToanVeThang.setText(String.valueOf(vt.getThanhToan()));
                    } else {
                        MessageDialogHelper.showMessageDialog(parentForm, "Vé Tháng chưa được cập nhật", "Thông báo");
                    }
                }
            }
            veThang();
        } catch (Exception e) {
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnGiaHanActionPerformed

