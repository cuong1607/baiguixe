    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaNV, sb, "Cần nhập Mã Nhân Viên");
        DataValidator.validateEmpty(txtTaiKhoan, sb, "Cần nhập Tài Khoản");
        DataValidator.validateEmpty(txtMatKhau, sb, "Cần nhập Mật Khẩu");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "lỗi");
            return;
        }
        try {
            NguoiDung nd = null;
            NguoiDungDao dao = new NguoiDungDao();
            nd = dao.findByNV(txtMaNV.getText());
            if (nd != null) {
                Integer option = MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn lưu tài khoản không?", "Thông Báo");
                if (option == JOptionPane.OK_OPTION) {
                    nd.setTenDangNhap(txtTaiKhoan.getText());
                    nd.setMatKhau(txtMatKhau.getText());
                    nd.setVaiTro((String) cbxChucVu.getSelectedItem());
                }
                if (dao.update(nd)) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Tài khoản đã được cập nhật", "Thông Báo");
                } else {
                    MessageDialogHelper.showMessageDialog(parentForm, "Không thể cập nhật tài khoản", "Thông Báo");
                }
            } else {
                nd = new NguoiDung(); 
                nd.setMaNV(txtMaNV.getText());
                nd.setTenDangNhap(txtTaiKhoan.getText());
                nd.setMatKhau(txtMatKhau.getText());
                nd.setVaiTro((String) cbxChucVu.getSelectedItem());
                if (dao.insert(nd)) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Tài khoản đã được lưu", "Thông Báo");

                } else {
                    MessageDialogHelper.showMessageDialog(parentForm, "Không thể Lưu tài khoản", "Thông Báo");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
        bangNguoiDung();
    }//GEN-LAST:event_btnLuuActionPerformed
