    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       StringBuilder sb = new StringBuilder();
       DataValidator.validateEmpty(txtUsername, sb, "Tên đăng nhập không được để trống");
       DataValidator.validateEmpty(txtPassword, sb, "Mật khẩu không được để trống");
       if (sb.length()>0){
           MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
           return;
       }
       NguoiDungDao dao = new NguoiDungDao();
       try {
           NguoiDung nd = dao.checkLogin(txtUsername.getText(), new String(txtPassword.getPassword()));
           if( nd == null){
               MessageDialogHelper.showErrorDialog(this,"Tên đăng nhập hoặc mật khẩu sai",  "Lỗi");
           }else {
               FormMain form = new FormMain();
               MessageDialogHelper.showMessageDialog(this,"Đăng nhập thành công",  "Thông Báo");
               Infomation instance = Infomation.getInstance();
               instance.setUsername(nd.getTenDangNhap());
               instance.setChucVu(nd.getVaiTro());
               instance.setPassword(nd.getMatKhau());
               instance.setRememberMe(chbRememberMe.isSelected());
               this.dispose();
               form.setVisible(true);
               form.setThongTin();
           }
       }catch(Exception e){
           MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
       }
    }//GEN-LAST:event_btnLoginActionPerformed

