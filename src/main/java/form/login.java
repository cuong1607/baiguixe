    private void rememberMe(){
        Infomation infomation = Infomation.getInstance();
        if(!infomation.isIsLogin() && !infomation.isRememberMe()){
            txtUsername.setText("");
            txtPassword.setText(""); 
            chbRememberMe.setSelected(false);
        }else{
            txtUsername.setText(infomation.getUsername());
            txtPassword.setText(infomation.getPassword());
            chbRememberMe.setSelected(true);
        }
    }
