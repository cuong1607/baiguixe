    private void btnVaoBenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVaoBenActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtTheXe, sb, "Cần nhập Thẻ Xe");
        DataValidator.validateEmpty(txtBienSoXe, sb, "Cần nhập Biển Số Xe");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "lỗi");
            return;
        }
        try {
            BaiXe bx = null;
            BaiXeDao dao = new BaiXeDao();
            VeThangDao vtDao = new VeThangDao();
            String theXe = (String) txtTheXe.getText();
            if(!dao.checkTheXe(theXe)){
                throw new Exception("Thẻ Xe Đã Được Dùng");
            }
            List<BaiXe> listBaiXe = dao.findByBienSoXe(txtBienSoXe.getText());
            if(listBaiXe != null && listBaiXe.size() > 0){
                throw new Exception("Xe Đang Ở Trong Bến");
            }
            List<VeThang> tx = vtDao.findByTheXe(theXe);
            if (tx != null && tx.size() > 0) {
                VeThang txx = tx.get(0);
                if (!txx.getBienSoXe().equals(txtBienSoXe.getText())) {
                    throw new Exception("Biển số xe không đúng!");
                }
                if (!txx.getLoaiXe().equals(cbxLoaiXe.getSelectedItem())) {
                    throw new Exception(" Loại xe không đúng!");
                }
                if (txx.getNgayKetThuc().getTime() < new Date().getTime()) {
                    throw new Exception(" Thẻ xe đã hết hạn!");
                }
                Integer option = MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn thêm xe vào không?", "Hỏi");
                if (option == JOptionPane.OK_OPTION) {
                    bx = new BaiXe();
                    bx.setBienSoXe(txtBienSoXe.getText());
                    bx.setTheXe(txtTheXe.getText());
                    bx.setLoaiXe((String)cbxLoaiXe.getSelectedItem());
                    bx.setLoaiVe(LoaiVe.THANG.getName());
                    bx.setThoiGianVao(new Date());
                    if (dao.insert(bx)) {
                        MessageDialogHelper.showMessageDialog(parentForm, "Đã được lưu", "Thông báo");
                        capNhatTTBaiXe((String)cbxLoaiXe.getSelectedItem(), -1);
                    } else {
                        MessageDialogHelper.showMessageDialog(parentForm, "Không thể lưu", "Thông báo");
                    }
                }
            } else {
                bx = new BaiXe();
                bx.setBienSoXe(txtBienSoXe.getText());
                bx.setTheXe(txtTheXe.getText());
                bx.setLoaiXe((String)cbxLoaiXe.getSelectedItem());
                bx.setLoaiVe(LoaiVe.LUOT.getName());
                bx.setThoiGianVao(new Date());
                if (dao.insert(bx)) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Đã được lưu", "Thông báo");
                    capNhatTTBaiXe((String)cbxLoaiXe.getSelectedItem(), -1);
                } else {
                    MessageDialogHelper.showMessageDialog(parentForm, "Không thể lưu", "Thông báo");
                }
            }
            baiXe();
        } catch (Exception e) {
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }

    }//GEN-LAST:event_btnVaoBenActionPerformed
    
    public void capNhatTTBaiXe(String loaiXe, int thayDoi) throws Exception{
        TTBaiXeDao dao = new TTBaiXeDao();
        TTBaiXe tt = dao.findByLoaiXe(loaiXe);
        int temp = tt.getSoChoTrong() + thayDoi;
        int soChoTrong = Math.max(0, temp);
        soChoTrong = Math.min(tt.getTongSoCho(), soChoTrong);
        tt.setSoChoTrong(soChoTrong);
        dao.update(tt);
    }
    
