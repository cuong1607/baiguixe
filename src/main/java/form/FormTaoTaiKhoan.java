    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        NguoiDungDao dao = new NguoiDungDao();
        try {
            if (!txtMaNV.getText().equals("")) {
                dao.delete(txtMaNV.getText());
                MessageDialogHelper.showMessageDialog(parentForm, "Xóa Thành Công!", "Thông Báo");
                txtMaNV.setText("");
                txtTaiKhoan.setText("");
                txtMatKhau.setText("");
            } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Hãy chọn dòng cần xóa!", "Thông Báo");
            }
            bangNguoiDung();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblTaoTaiKhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTaoTaiKhoanMouseClicked
        int row = tblTaoTaiKhoan.getSelectedRow();
        if (row >= 0) {
            txtMaNV.setText(tblTaoTaiKhoan.getValueAt(row, 0).toString());
            txtTaiKhoan.setText(tblTaoTaiKhoan.getValueAt(row, 1).toString());
            txtMatKhau.setText(tblTaoTaiKhoan.getValueAt(row, 2).toString());
            cbxChucVu.setSelectedItem(tblTaoTaiKhoan.getValueAt(row, 3).toString());
        }
        setEdit(false);
    }//GEN-LAST:event_tblTaoTaiKhoanMouseClicked


